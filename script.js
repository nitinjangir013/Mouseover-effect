// Card First
$(".card_1").hover(function(){
  $(this).css({
    filter:"grayscale(0%)",
    color:'#fff',
    'margin-top': '-20px',
    'box-shadow': '0 10px 10px 10px rgba(0, 0, 0, 0.1)'
  });
  $(".i_d1").css({
    'margin-top':'10px'
  });
  $(".button_1").text('Click here ->');
  }, function(){
  $(this).css({
    filter:"grayscale(100%)",
    color:'#000',
    'margin-top': '0px',
    'box-shadow': '0 0px 10px 0px rgba(0, 0, 0, 0.1)'
  });
  $(".i_d1").css({
    'margin-top':'30px'
  });
  $(".button_1").text('');
});


